package Program;

public class Switch {
public static void main(String[] args) {
	int a=5,b=3;
	switch('*')
	{
	case '-':int y=a-b;
	          System.out.println(y);
	          break;
	case '+':int x=a+b;
	       System.out.println(x);
	       break;
	case '*':int z=a*b;
    System.out.println(z);
    break;
     
	          
	}
}
}
